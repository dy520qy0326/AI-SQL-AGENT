"""Tests for the SQL migration script generator."""

from app.diff.migration import generate_alter_scripts


def _diff(tables_added=None, tables_removed=None, fields_added=None,
          fields_removed=None, fields_modified=None, indexes_added=None,
          indexes_removed=None, relations_added=None, relations_removed=None,
          breaking_details=None):
    return {
        "tables_added": tables_added or [],
        "tables_removed": tables_removed or [],
        "tables_renamed": [],
        "fields_added": fields_added or [],
        "fields_removed": fields_removed or [],
        "fields_modified": fields_modified or [],
        "fields_renamed": [],
        "indexes_added": indexes_added or [],
        "indexes_removed": indexes_removed or [],
        "relations_added": relations_added or [],
        "relations_removed": relations_removed or [],
        "breaking_changes": bool(breaking_details),
        "breaking_details": breaking_details or [],
    }


class TestNoChanges:
    def test_empty_diff(self):
        sql = generate_alter_scripts(_diff())
        assert "No schema changes detected" in sql

    def test_comment_header(self):
        sql = generate_alter_scripts(_diff(fields_added=[{"table": "t", "field": "x", "definition": {"name": "x", "type": "int"}}]))
        assert "Migration generated by AI SQL Agent" in sql


class TestTableChanges:
    def test_create_table(self):
        diff = _diff(tables_added=[{
            "name": "coupons",
            "columns": [
                {"name": "id", "type": "int", "nullable": False, "primary_key": True},
                {"name": "code", "type": "varchar", "length": 50, "nullable": True},
            ],
            "indexes": [],
            "foreign_keys": [],
        }])
        sql = generate_alter_scripts(diff)
        assert "CREATE TABLE coupons" in sql
        assert "id int NOT NULL" in sql
        assert "code varchar(50)" in sql
        assert "PRIMARY KEY" in sql

    def test_drop_table(self):
        diff = _diff(tables_removed=[{"name": "old_table"}])
        sql = generate_alter_scripts(diff)
        assert "DROP TABLE IF EXISTS old_table" in sql


class TestFieldChanges:
    def test_add_field(self):
        diff = _diff(fields_added=[{
            "table": "orders",
            "field": "coupon_id",
            "definition": {"name": "coupon_id", "type": "int", "nullable": True},
        }])
        sql = generate_alter_scripts(diff)
        assert "ALTER TABLE orders ADD COLUMN coupon_id int" in sql

    def test_drop_field(self):
        diff = _diff(fields_removed=[{"table": "orders", "field": "old_col"}])
        sql = generate_alter_scripts(diff)
        assert "ALTER TABLE orders DROP COLUMN old_col" in sql

    def test_modify_field_mysql(self):
        diff = _diff(fields_modified=[{
            "table": "users",
            "field": "name",
            "before": {"type": "varchar", "length": 100, "nullable": True, "default": None},
            "after": {"type": "varchar", "length": 255, "nullable": True, "default": None},
        }])
        sql = generate_alter_scripts(diff, "mysql")
        assert "MODIFY COLUMN name varchar(255) NULL" in sql

    def test_modify_field_postgresql(self):
        diff = _diff(fields_modified=[{
            "table": "users",
            "field": "name",
            "before": {"type": "varchar", "length": 100, "nullable": True, "default": None},
            "after": {"type": "varchar", "length": 255, "nullable": True, "default": None},
        }])
        sql = generate_alter_scripts(diff, "postgresql")
        assert "ALTER COLUMN name TYPE varchar(255)" in sql


