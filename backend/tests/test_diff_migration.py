class TestRelationChanges:
    def test_add_foreign_key(self):
        diff = _diff(relations_added=[{
            "table": "orders",
            "relation": {"columns": ["user_id"], "ref_table": "users", "ref_columns": ["id"]},
        }])
        sql = generate_alter_scripts(diff)
        assert "ALTER TABLE orders ADD FOREIGN KEY (user_id) REFERENCES users(id)" in sql

    def test_remove_foreign_key_with_constraint_name(self):
        diff = _diff(relations_removed=[{
            "table": "orders",
            "relation": {"columns": ["user_id"], "ref_table": "users", "ref_columns": ["id"], "constraint_name": "fk_orders_user"},
        }])
        sql = generate_alter_scripts(diff, "mysql")
        assert "DROP FOREIGN KEY fk_orders_user" in sql

    def test_remove_foreign_key_postgresql(self):
        diff = _diff(relations_removed=[{
            "table": "orders",
            "relation": {"columns": ["user_id"], "ref_table": "users", "ref_columns": ["id"], "constraint_name": "fk_orders_user"},
        }])
        sql = generate_alter_scripts(diff, "postgresql")
        assert "DROP CONSTRAINT IF EXISTS fk_orders_user" in sql


class TestBreakingMarkers:
    def test_breaking_changes_marked(self):
        diff = _diff(
            tables_removed=[{"name": "obsolete"}],
            breaking_details=["table 'obsolete' removed"],
        )
        sql = generate_alter_scripts(diff)
        assert "destructive changes" in sql
        assert "WARNING: table 'obsolete' removed" in sql

    def test_non_breaking_no_marker(self):
        diff = _diff(fields_added=[{"table": "t", "field": "x", "definition": {"name": "x", "type": "int"}}])
        sql = generate_alter_scripts(diff)
        assert "destructive changes" not in sql


class TestDialectSpecific:
    def test_mysql_modify_column_uses_modify(self):
        diff = _diff(fields_modified=[{
            "table": "t",
            "field": "c",
            "before": {"type": "int", "nullable": False},
            "after": {"type": "bigint", "nullable": False},
        }])
        sql = generate_alter_scripts(diff, "mysql")
        assert "MODIFY COLUMN" in sql

    def test_postgresql_alter_column_uses_type(self):
        diff = _diff(fields_modified=[{
            "table": "t",
            "field": "c",
            "before": {"type": "int", "nullable": False},
            "after": {"type": "bigint", "nullable": False},
        }])
        sql = generate_alter_scripts(diff, "postgresql")
        assert "ALTER COLUMN c TYPE bigint" in sql

    def test_dialect_defaults_to_mysql(self):
        diff = _diff(fields_modified=[{
            "table": "t",
            "field": "c",
            "before": {"type": "int", "nullable": False},
            "after": {"type": "bigint", "nullable": False},
        }])
        sql = generate_alter_scripts(diff)
        assert "MODIFY COLUMN" in sql
